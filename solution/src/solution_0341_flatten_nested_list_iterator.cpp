class NestedIterator {
private:
    vector<int> ans;
    int i = 0;

public:
    void helper(vector<NestedInteger> &nestedList) {
        for(int id = 0; id < nestedList.size(); id++) {
            if(nestedList[id].isInteger())
                ans.push_back(nestedList[id].getInteger());
            else
                helper(nestedList[id].getList());
        }

    }

    NestedIterator(vector<NestedInteger> &nestedList) {
        helper(nestedList);
    }

    int next() {
        return ans[i++];
    }

    bool hasNext() {
        return i != ans.size();
    }
};

